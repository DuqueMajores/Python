"""
Crie um programa que receba dois números e mostre:

- a soma
- a subtração 
- a multiplicação
- e a divisão entre eles.

"""

num1 = int(input("Escreva um número: "))
num2 = int(input("Escreva outro: "))

print("soma -> ", num1 + num2)
print("Sub. -> ", num1 - num2)
print("Multi. -> ", num1 * num2)
print("Divi. -> ", num1 / num2)
